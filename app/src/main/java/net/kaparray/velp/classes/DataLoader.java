package net.kaparray.velp.classes;


public class DataLoader {

    public String UID;
    public String nameUser;
    public String phoneUser;
    public String reatingUser;
    public String pointsUser;
    public String passwordUser;

    public DataLoader(String UID, String nameUser, String phoneUser,
                      String reatingUser, String pointsUser, String passwordUser) {
        this.UID = UID;
        this.nameUser = nameUser;
        this.phoneUser = phoneUser;
        this.reatingUser = reatingUser;
        this.pointsUser = pointsUser;
        this.passwordUser = passwordUser;
    }




}
